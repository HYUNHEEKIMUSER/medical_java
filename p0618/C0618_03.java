package p0618;

public class C0618_03 {

	public static void main(String[] args) {
		int i = 0;
		while(i<10) {
			System.out.println(i);
			i++;
		}
		//초기화; 조건식; 증감식
		for(int i=0; i<10; i++) {
			System.out.println(i);
		}
		//거꾸로
		int i = 10;
		while(i>0) {
			System.out.println(i);
			i--;
		}
		for(int i=10; i>0; i--) {
			System.out.println(i);
		// 반복문 for 중복, while에 이름을 부여할 수 있다.
		//while(true)->무한반복	
		while(true) {
			
		}
		
		//for(;;)도 무한반복
		for(;;) {
			
		}
		}
	}

}
